public class Ceiling {
    private int height;
    private String paintedColour;

    public Ceiling(int height, java.lang.String paintedColour) {
        this.height = height;
        this.paintedColour = paintedColour;
    }

    public int getHeight() {
        return height;
    }

    public java.lang.String getPaintedColour() {
        return paintedColour;
    }
}
